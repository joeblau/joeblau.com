/**
 * Stateful token selector used for both the "From" and "To" sides. Before a
 * token is picked it shows a "From..." / "To..." placeholder; picking a token
 * swaps in a stacked icon (Boring Avatar + chain + asset) with the address /
 * chain / name plus side-specific meta (Test/Max + holdings for From,
 * Slippage/Fee for To). The "To" drawer adds an enter/paste address field with
 * a QR scanner. Static data — asset/chain icons come from Relay's icon CDN and
 * the address avatar is generated with boring-avatars.
 */

type Variant = "from" | "to";

export interface TokenRow {
	name: string;
	symbol: string;
	chain: string;
	chainId: number;
	address: string;
	logo: string;
	amount: string;
	usd: string;
	/** Token decimals — required to convert display amounts to on-chain base units. */
	decimals: number;
	/** Chain VM family, for routing execution (EVM today; SVM guarded). */
	vmType?: "evm" | "svm";
}

/** USD price per unit of a token: total usd ÷ holdings. 0 if NaN / divide-by-zero. */
export function price(token: TokenRow) {
	const usd = Number.parseFloat(token.usd.replace(/[^0-9.]/g, "")) || 0;
	const amount = Number.parseFloat(token.amount) || 0;
	if (!(usd > 0) || !(amount > 0)) return 0;
	return usd / amount;
}

